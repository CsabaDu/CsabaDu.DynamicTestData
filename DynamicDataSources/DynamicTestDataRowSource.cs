        expected,
        arg1, arg2, arg3, arg4, arg5, arg6, arg7)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataThrowsToTestDataRow{TException, T1, T2, T3, T4, T5, T6, T7}" />
    /// <typeparam name="T8">The type of the eighth argument.</typeparam>
    /// <param name="arg8">The eighth argument.</param>
    protected TestDataRow<TestDataThrows<TException, T1, T2, T3, T4, T5, T6, T7, T8>> TestDataThrowsToTestDataRow<TException, T1, T2, T3, T4, T5, T6, T7, T8>(
