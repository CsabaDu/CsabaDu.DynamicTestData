namespace CsabaDu.DynamicTestData.DynamicDataSources;

public abstract class DynamicTestDataRowSource(ArgsCode argsCode)
: DynamicDataSourceBase(argsCode)
{
    #region Methods
    #region OptionalToTestDataRow
    /// <summary>
    /// Executes the provided test data function with an optional temporary ArgsCode override.
    /// </summary>
    /// <param name="testDataToTestDataRow">A function that generates test data arguments.</param>
    /// <param name="argsCode">An optional ArgsCode to temporarily use during the execution of <paramref name="testDataToTestDataRow"/>.</param>
    /// <returns>The array of arguments generated by <paramref name="testDataToTestDataRow"/>.</returns>
    /// <remarks>
    /// If <paramref name="argsCode"/> is provided, it will be used during the execution of <paramref name="testDataToTestDataRow"/>
    /// and then automatically restored to the previous value afterward.
    /// </remarks>
    protected ITestDataRow OptionalToTestDataRow(Func<ITestDataRow> testDataToTestDataRow, ArgsCode? argsCode)
    {
        ArgumentNullException.ThrowIfNull(testDataToTestDataRow, nameof(testDataToTestDataRow));
        return WithOptionalArgsCode(this, testDataToTestDataRow, argsCode);
    }
    #endregion

    #region TestDataToTestDataRow
    /// <summary>
    /// Converts test data to a test data row.
    /// </summary>
    /// <typeparam name="T1">The type of the first argument.</typeparam>
    /// <param name="definition">The definition of the test data.</param>
    /// <param name="expected">The expected result of the test.</param>
    /// <param name="arg1">The first argument.</param>
    /// <returns>An array of arguments.</returns>
    protected TestDataRow<TestData<T1>> TestDataToTestDataRow<T1>(
        string definition,
        string expected,
        T1? arg1)
    => new TestData<T1>(
        definition,
        expected,
        arg1)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataToTestDataRow{T1}" />
    /// <typeparam name="T2">The type of the second argument.</typeparam>
    /// <param name="arg2">The second argument.</param>
    /// <returns>An array of arguments.</returns>
    protected TestDataRow<TestData<T1, T2>> TestDataToTestDataRow<T1, T2>(
        string definition,
        string expected,
        T1? arg1, T2? arg2)
    => new TestData<T1, T2>(
        definition,
        expected,
        arg1, arg2)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataToTestDataRow{T1, T2}" />
    /// <typeparam name="T3">The type of the third argument.</typeparam>
    /// <param name="arg3">The third argument.</param>
    /// <returns>An array of arguments.</returns>
    protected TestDataRow<TestData<T1, T2, T3>> TestDataToTestDataRow<T1, T2, T3>(
        string definition,
        string expected,
        T1? arg1, T2? arg2, T3? arg3)
    => new TestData<T1, T2, T3>(
        definition,
        expected,
        arg1, arg2, arg3)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataToTestDataRow{T1, T2, T3}" />
    /// <typeparam name="T4">The type of the fourth argument.</typeparam>
    /// <param name="arg4">The fourth argument.</param>
    /// <returns>An array of arguments.</returns>
    protected TestDataRow<TestData<T1, T2, T3, T4>> TestDataToTestDataRow<T1, T2, T3, T4>(
        string definition,
        string expected,
        T1? arg1, T2? arg2, T3? arg3, T4? arg4)
    => new TestData<T1, T2, T3, T4>(
        definition,
        expected,
        arg1, arg2, arg3, arg4)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataToTestDataRow{T1, T2, T3, T4}" />
    /// <typeparam name="T5">The type of the fifth argument.</typeparam>
    /// <param name="arg5">The fifth argument.</param>
    /// <returns>An array of arguments.</returns>
    protected TestDataRow<TestData<T1, T2, T3, T4, T5>> TestDataToTestDataRow<T1, T2, T3, T4, T5>(
        string definition,
        string expected,
        T1? arg1, T2? arg2, T3? arg3, T4? arg4, T5? arg5)
    => new TestData<T1, T2, T3, T4, T5>(
        definition,
        expected,
        arg1, arg2, arg3, arg4, arg5)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataToTestDataRow{T1, T2, T3, T4, T5}" />
    /// <typeparam name="T6">The type of the sixth argument.</typeparam>
    /// <param name="arg6">The sixth argument.</param>
    /// <returns>An array of arguments.</returns>
    protected TestDataRow<TestData<T1, T2, T3, T4, T5, T6>> TestDataToTestDataRow<T1, T2, T3, T4, T5, T6>(
        string definition,
        string expected,
        T1? arg1, T2? arg2, T3? arg3, T4? arg4, T5? arg5, T6? arg6)
    => new TestData<T1, T2, T3, T4, T5, T6>(
        definition,
        expected,
        arg1, arg2, arg3, arg4, arg5, arg6)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataToTestDataRow{T1, T2, T3, T4, T5, T6}" />
    /// <typeparam name="T7">The type of the seventh argument.</typeparam>
    /// <param name="arg7">The seventh argument.</param>
    /// <returns>An array of arguments.</returns>
    protected TestDataRow<TestData<T1, T2, T3, T4, T5, T6, T7>> TestDataToTestDataRow<T1, T2, T3, T4, T5, T6, T7>(
        string definition,
        string expected,
        T1? arg1, T2? arg2, T3? arg3, T4? arg4, T5? arg5, T6? arg6, T7? arg7)
