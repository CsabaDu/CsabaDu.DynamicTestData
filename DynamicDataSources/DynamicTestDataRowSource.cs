    /// <param name="arg7">The seventh argument.</param>
    protected TestDataRow<TestDataThrows<TException, T1, T2, T3, T4, T5, T6, T7>> TestDataThrowsToTestDataRow<TException, T1, T2, T3, T4, T5, T6, T7>(
