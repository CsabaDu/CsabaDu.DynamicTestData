        arg1, arg2, arg3, arg4, arg5, arg6, arg7, arg8)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataThrowsToTestDataRow{TException, T1, T2, T3, T4, T5, T6, T7, T8}" />
    /// <typeparam name="T9">The type of the ninth argument.</typeparam>
    /// <param name="arg9">The ninth argument.</param>
    protected TestDataRow<TestDataThrows<TException, T1, T2, T3, T4, T5, T6, T7, T8, T9>> TestDataThrowsToTestDataRow<TException, T1, T2, T3, T4, T5, T6, T7, T8, T9>(
