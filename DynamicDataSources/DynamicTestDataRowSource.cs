        string definition,
        TException expected,
        T1? arg1, T2? arg2, T3? arg3)
    where TException : Exception
    => new TestDataThrows<TException, T1, T2, T3>(
        definition,
        expected,
        arg1, arg2, arg3)
        .ToTestDataRow(ArgsCode);

    /// <inheritdoc cref="TestDataThrowsToTestDataRow{TException, T1, T2, T3}" />
    /// <typeparam name="T4">The type of the fourth argument.</typeparam>
    /// <param name="arg4">The fourth argument.</param>
    protected TestDataRow<TestDataThrows<TException, T1, T2, T3, T4>> TestDataThrowsToTestDataRow<TException, T1, T2, T3, T4>(
