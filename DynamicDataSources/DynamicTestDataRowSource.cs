    /// <param name="arg5">The fifth argument.</param>
    protected TestDataRow<TestDataThrows<TException, T1, T2, T3, T4, T5>> TestDataThrowsToTestDataRow<TException, T1, T2, T3, T4, T5>(
        string definition,
        TException expected,
